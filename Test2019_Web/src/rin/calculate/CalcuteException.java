package rin.calculate;

public class CalcuteException extends Exception {

	private static final long serialVersionUID = 7527520921209155837L;

    public CalcuteException() {
        super();
    }

    public CalcuteException(String message) {
        super(message);
    }

    public CalcuteException(String message, Throwable cause) {
        super(message, cause);
    }

    public CalcuteException(Throwable cause) {
        super(cause);
    }
	
	
}
